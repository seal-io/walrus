package generators

import (
	"os"
	"path/filepath"
	"strings"
	"testing"

	"github.com/stretchr/testify/assert"
	"k8s.io/gengo/args"
	"k8s.io/gengo/generator"

	webhookgenargs "github.com/seal-io/code-generator/cmd/webhook-gen/args"
)

func Test_webhookGen(t *testing.T) {
	var (
		dir = filepath.Join("testdata", "webhook_gen")
		pkg = "github.com/seal-io/code-generator/cmd/webhook-gen/generators/testdata/webhook_gen"
	)

	g := args.GeneratorArgs{
		InputDirs:          []string{pkg},
		OutputBase:         filepath.Join("testdata", "webhook_gen"),
		OutputFileBaseName: "zz_generated.webhooks",
		GoHeaderFilePath:   filepath.Join(dir, "go.txt"),
		GeneratedBuildTag:  "ignore_autogenerated",
		CustomArgs:         &webhookgenargs.CustomArgs{},
	}

	err := g.Execute(
		NameSystems(),
		DefaultNameSystem(),
		func(ctx *generator.Context, args *args.GeneratorArgs) generator.Packages {
			packages := Packages(ctx, args)

			for i := range packages {
				p, ok := packages[i].(*generator.DefaultPackage)
				if !ok {
					continue
				}

				p.PackagePath = strings.TrimPrefix(p.PackagePath, pkg)
				packages[i] = p
			}

			return packages
		})
	if err != nil {
		t.Fatalf("failed to execute webhook-gen: %v", err)
	}

	actualBytes, err := os.ReadFile(filepath.Join(dir, "zz_generated.webhooks.go"))
	if err != nil {
		t.Fatalf("failed to read actual file: %v", err)
	}

	expectedBytes, err := os.ReadFile(filepath.Join(dir, "zz_generated.webhooks.go.expected"))
	if err != nil {
		t.Fatalf("failed to read expected file: %v", err)
	}

	assert.Equal(t, string(expectedBytes), string(actualBytes))
}
